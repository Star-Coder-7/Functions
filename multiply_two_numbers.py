def multiply(x: float, y: float) -> float:
    """
    Multiplying two numbers, `x` and `y`. They can be an int or a float.
    This function is indented to multiply two numbers, `x` and `y`
    (they are obviously going to be replaced by the user).

    :param x: The first number to multiply.
    :param y: The multiplier of `x`.
    :return: The product of `x` and `y`.
    """
    result = x * y
    return result


answer = multiply(26.9, 82.5)
